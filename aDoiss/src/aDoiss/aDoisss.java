package aDoiss;

public class aDoisss {

	public static void main(String[] args) {
		
		int nume = 10;
		int nCont = 1;
		
		while (nume <= 100) {
			
			System.out.println("Numero "+ nume);
			
			while (nCont <= 7) {
				System.out.println("numero "+ nCont);
				nCont ++;
				
			}
			nume = nume + 10;
			nCont = 1;
		}

	}

}
